public class Wrapper {
  
  private String entry1;
  private String entry2;
  
  public Wrapper(String pEntry1, String pEntry2) {
    entry1 = pEntry1;
    entry2 = pEntry2;
  }

  public String getEntry1(){
    return entry1;   
  }
  public String getEntry2(){
    return entry2;  
  }
  
}
